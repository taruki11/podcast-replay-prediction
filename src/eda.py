import sys
sys.stdout.reconfigure(encoding='utf-8')
import pandas as pd
import os

base = r'D:\Pycharm_workplace\即刻笔试\data\Iftech\dataset_algo2025'

# === TRAIN ===
train = pd.read_csv(os.path.join(base, 'train.csv'))
print('='*60)
print('TRAIN.CSV')
print('='*60)
print(f'Shape: {train.shape}')
print(f'Label distribution:\n{train["label"].value_counts()}')
print(f'Positive rate: {train["label"].mean():.4f}')
print(f'Unique uid: {train["uid"].nunique()}')
print(f'Unique episode_id: {train["episode_id"].nunique()}')
print(f'tab_name: {train["tab_name"].value_counts().to_dict()}')
print(f'scene_name (incl NaN): {train["scene_name"].value_counts(dropna=False).to_dict()}')
print(f'entrance_type (incl NaN): {train["entrance_type"].value_counts(dropna=False).to_dict()}')
print(f'Missing values:\n{train.isnull().sum()}')
all_ctx_empty = (train['tab_name'].isna() & train['scene_name'].isna() & train['entrance_type'].isna()).sum()
print(f'Rows with ALL context fields empty: {all_ctx_empty} ({all_ctx_empty/len(train)*100:.1f}%)')
print()

# === TEST ===
test = pd.read_csv(os.path.join(base, 'test.csv'))
print('='*60)
print('TEST.CSV')
print('='*60)
print(f'Shape: {test.shape}')
print(f'Unique uid: {test["uid"].nunique()}')
print(f'Unique episode_id: {test["episode_id"].nunique()}')
train_uids = set(train['uid'].unique())
test_uids = set(test['uid'].unique())
train_eps = set(train['episode_id'].unique())
test_eps = set(test['episode_id'].unique())
print(f'UID overlap with train: {len(train_uids & test_uids)} / {len(test_uids)} test uids ({len(train_uids & test_uids)/len(test_uids)*100:.1f}%)')
print(f'Episode overlap with train: {len(train_eps & test_eps)} / {len(test_eps)} test episodes ({len(train_eps & test_eps)/len(test_eps)*100:.1f}%)')
print(f'New uids (cold-start): {len(test_uids - train_uids)} ({len(test_uids - train_uids)/len(test_uids)*100:.1f}%)')
print(f'New episodes (cold-start): {len(test_eps - train_eps)} ({len(test_eps - train_eps)/len(test_eps)*100:.1f}%)')
print(f'Missing values:\n{test.isnull().sum()}')
print()

# === USER FEATURE ===
uf = pd.read_csv(os.path.join(base, 'user_feature.csv'))
print('='*60)
print('USER_FEATURE.CSV')
print('='*60)
print(f'Shape: {uf.shape}')
print(f'Unique uid: {uf["uid"].nunique()}')
print(f'Age: mean={uf["age"].mean():.1f}, median={uf["age"].median():.1f}, min={uf["age"].min()}, max={uf["age"].max()}')
age_zero = (uf['age']==0).sum()
print(f'Age=0 (sentinel): {age_zero} ({age_zero/len(uf)*100:.1f}%)')
print(f'Sex:\n{uf["sex"].value_counts(dropna=False)}')
print(f'rg_source:\n{uf["rg_source"].value_counts()}')
print(f'address:\n{uf["address"].value_counts()}')
print(f'Missing:\n{uf.isnull().sum()}')
complete = ((uf['age']>0) & (uf['sex'].notna()) & (uf['sex']!='')).sum()
print(f'Users with real age+sex: {complete} ({complete/len(uf)*100:.1f}%)')
print()

# === EPISODE FEATURE ===
ef = pd.read_csv(os.path.join(base, 'episode_feature.csv'))
print('='*60)
print('EPISODE_FEATURE.CSV')
print('='*60)
print(f'Shape: {ef.shape}')
print(f'Unique episode_id: {ef["episode_id"].nunique()}')
dur_sec = ef['duration'] / 1000
print(f'Duration (sec): mean={dur_sec.mean():.0f}, median={dur_sec.median():.0f}, min={dur_sec.min():.0f}, max={dur_sec.max():.0f}')
dur_min = dur_sec / 60
print(f'Duration (min): mean={dur_min.mean():.1f}, median={dur_min.median():.1f}, min={dur_min.min():.1f}, max={dur_min.max():.1f}')
print(f'Language:\n{ef["language"].value_counts(dropna=False)}')
cat_counts = ef['category_ids'].dropna().str.split('|').explode().value_counts()
print(f'Top 15 categories:\n{cat_counts.head(15)}')
cats_per = ef['category_ids'].dropna().str.split('|').str.len()
print(f'Categories/episode: mean={cats_per.mean():.1f}, max={cats_per.max()}, min={cats_per.min()}')
host_lens = ef['host'].dropna().str.split('|').str.len()
print(f'Hosts/episode: mean={host_lens.mean():.1f}, max={host_lens.max()}')
all_hosts = ef['host'].dropna().str.split('|').explode()
print(f'Unique hosts: {all_hosts.nunique()}, top 10:\n{all_hosts.value_counts().head(10)}')
all_prod = ef['producer'].dropna().str.split('|').explode()
print(f'Unique producers: {all_prod.nunique()}, top 5:\n{all_prod.value_counts().head(5)}')
all_writ = ef['writer'].dropna().str.split('|').explode()
print(f'Unique writers: {all_writ.nunique()}, top 5:\n{all_writ.value_counts().head(5)}')
print(f'Missing:\n{ef.isnull().sum()}')
print()

# === EPISODE ADDITIONAL ===
ea = pd.read_csv(os.path.join(base, 'episode_additional.csv'))
print('='*60)
print('EPISODE_ADDITIONAL.CSV')
print('='*60)
print(f'Shape: {ea.shape}')
print(f'Unique episode_id: {ea["episode_id"].nunique()}')
title_lens = ea['title'].dropna().str.split('|').str.len()
print(f'Title segments: mean={title_lens.mean():.1f}, median={title_lens.median():.0f}, min={title_lens.min()}, max={title_lens.max()}')
title_tokens = ea['title'].dropna().str.split('|').explode()
print(f'Unique title tokens: {title_tokens.nunique()}, top 10:\n{title_tokens.value_counts().head(10)}')
print(f'Unique uuid: {ea["uuid"].nunique()} / {len(ea)} episodes')
uuid_counts = ea['uuid'].value_counts()
print(f'UUID group sizes: mean={uuid_counts.mean():.1f}, median={uuid_counts.median():.0f}, max={uuid_counts.max()}')
print(f'Singleton uuids: {(uuid_counts==1).sum()}')
nan_uuid = ea['uuid'].str.contains('nan', na=False).sum()
print(f'UUIDs containing nan: {nan_uuid}')
print(f'Missing:\n{ea.isnull().sum()}')
print()

# === CROSS-TABLE ===
print('='*60)
print('CROSS-TABLE COVERAGE')
print('='*60)
all_uids = train_uids | test_uids
uf_uids = set(uf['uid'].unique())
print(f'All uids in user_feature: {len(all_uids & uf_uids)} / {len(all_uids)} ({len(all_uids & uf_uids)/len(all_uids)*100:.1f}%)')
all_eps = train_eps | test_eps
ef_eps = set(ef['episode_id'].unique())
ea_eps = set(ea['episode_id'].unique())
print(f'All episodes in episode_feature: {len(all_eps & ef_eps)} / {len(all_eps)} ({len(all_eps & ef_eps)/len(all_eps)*100:.1f}%)')
print(f'All episodes in episode_additional: {len(all_eps & ea_eps)} / {len(all_eps)} ({len(all_eps & ea_eps)/len(all_eps)*100:.1f}%)')
